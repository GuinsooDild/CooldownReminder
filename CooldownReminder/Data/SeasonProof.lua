local function BuildEntries(firstSeenSec, cdSeriesSec, defaultDuration)
    local entries = {}
    if not firstSeenSec then
        return entries
    end

    local current = firstSeenSec
    entries[#entries + 1] = { time = current, duration = defaultDuration }

    if type(cdSeriesSec) == "table" then
        for _, delta in ipairs(cdSeriesSec) do
            current = current + delta
            entries[#entries + 1] = { time = current, duration = defaultDuration }
        end
    end

    return entries
end

local function ApplyProofCoverage(dungeonKey, encounterOrder, source)
    local dungeon = NPR.Data and NPR.Data.dungeons and NPR.Data.dungeons[dungeonKey]
    if not dungeon then
        return
    end

    dungeon.encounterOrder = encounterOrder
    dungeon.mappedBossCount = #dungeon.encounterOrder
    dungeon.incompleteNote = nil
    dungeon.source = source
    dungeon.coverageState = "partial"
    dungeon.coverageNotes = "Only the listed proof bosses have timeline rows. Remaining main bosses stay registry-only until live or stronger reference timing is captured."
end

